package beans;

public class ChoixAdmission {

	private int id,idDossierEtudiant;
	private String choixProgrammes;
	private String choixSessions;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdDossierEtudiant() {
		return idDossierEtudiant;
	}
	public void setIdDossierEtudiant(int idDossierEtudiant) {
		this.idDossierEtudiant = idDossierEtudiant;
	}
	public String getChoixProgrammes() {
		return choixProgrammes;
	}
	public void setChoixProgrammes(String choixProgrammes) {
		this.choixProgrammes = choixProgrammes;
	}
	public String getChoixSessions() {
		return choixSessions;
	}
	public void setChoixSessions(String choixSessions) {
		this.choixSessions = choixSessions;
	}
	
	
}
